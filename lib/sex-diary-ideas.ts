// Helper to get random set of ideas for the "game" based on gender
export const getRandomIdeas = (gender: 'man' | 'woman' | 'other', count: number = 4) => {
    // Filter ideas relevant to the user's gender
    // 'man' sees 'man' + 'both'
    // 'woman' sees 'woman' + 'both'
    const filtered = AllIdeas.filter(idea => {
        if (idea.gender === 'both') return true;
        if (gender === 'man') return idea.gender === 'man';
        if (gender === 'woman') return idea.gender === 'woman';
        return true; // Fallback for 'other' or unspecified
    });

    const shuffled = [...filtered].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
};
